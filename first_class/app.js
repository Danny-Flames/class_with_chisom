
// Variables - are like containers used to store data. These data can be anything
// In javascript, you 'declare' a variable using these keywords:  var, let, const

var myName = "Daniel";
let myName2 = "Chisom";
const myBiography = "My name is emmanuel and i am 10yrs old. I live in Lagos, Nigeria.";

console.log(myName)
console.log(myName2)
console.log(myBiography)

